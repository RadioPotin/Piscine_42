#define MAJUS(c) (c >= 'A' && c <= 'Z' ? 1 : 0)

int		ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	if (str == 0)
		return (1);
	while (str[i])
	{
		if (!(MAJUS(str[i])))
			return (0);
		i++;
	}
	return (1);
}
