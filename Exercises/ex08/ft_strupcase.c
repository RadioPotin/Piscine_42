char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	if (str == 0)
		return (0);
	while (str[i])
	{
		if (str[i] <= 64 || (str[i] >= 91 && str[i] <= 96) || str[i] >= 124)
			i++;
		if (str[i] >= 65 && str[i] <= 90)
			i++;
		if (str[i] >= 97 && str[i] <= 122)
		{
			str[i] -= 32;
			i++;
		}
	}
	return (str);
}
